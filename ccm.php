                            Climb Milling/Conventional Milling
                            <a
                                href="https://www.harveyperformance.com/in-the-loupe/conventional-vs-climb-milling/">reference</a><br>
                            <br>
                            We also learned to understand the different properties compared to conventional and ascent
                            milling. <br>
                            <br>
                            <em>Conventional Milling</em><br>
                        <ul>
                            - Chip width starts from zero and increases which causes more heat to diffuse into
                            the workpiece and produces work hardening.<br>
                            - Tool rubs more at the beginning of the cut causing faster tool wear and decreases
                            tool life.<br>
                            - Chips are carried upward by the tooth and fall in front of cutter creating a marred finish
                            and re-cutting of chips.<br>
                            - Upwards forces created in horizontal milling* tend to lift the workpiece, more intricate
                            and expansive work holdings are needed to lessen the lift created.<br><br>

                            <div class="col-lg-3">
                                <img src="assets/img/week07/group_assigment/conventional_milling.jpg" class="img-fluid"
                                    alt="">
                            </div><br>

                        </ul>
                        <em>Climb Milling</em><br>
                        <ul>
                            - Hip width starts from maximum and decreases so heat generated will more likely transfer to
                            the chip. <br>
                            - Creates cleaner shear plane which causes the tool to rub less and increases tool life.<br>
                            - Chips are removed behind the cutter which reduces the chance of recutting.<br>
                            - Downwards forces in horizontal milling is created that helps hold the workpiece down, less
                            complex work holdings are need when coupled with these forces.<br>
                            - Horizontal milling is when the center line of the tool is parallel to the work piece.


                            <div class="col-lg-3">
                                <img src="assets/img/week07/group_assigment/climb_milling.jpg" class="img-fluid" alt="">
