                        <p>
                            <strong>Testing runout</strong><br><br>
                            Runout occurs when the tool on a CNC machine runs off of the correct axis, increasing the
                            potential for errors to occur throughout the milling process and drastically reducing the
                            expected lifetime of the tool. There are two types of runout: axial runout and radial
                            runout : <br><br>
                            Runout is important to avoid since it can decrease tool life and can increase the chance of
                            machining defective parts. Runount may result from excess debris, dirt, coolant that enters
                            the spindle and interferes with the clamped tool, which often results in defective machinery
                            parts or shortened tool life. There are several ways to mitigate tool runout, including
                            using higher quality tool holders, performing frequent maintenance and cleaning on the bit,
                            and decreasing the stickout of the bit.
                        </p>
                        <div class="row">
                            <div class="col-lg-6">
                                <p>
                                    <li>Axial Runout Axial runout is when the bit is not perfectly perpendicular to the
                                        surface you are attempting to cut. This can cause rotation in the bit that is
                                        not
                                        good for a cut. If you are using a flat end mill bit, normally you would like a
                                        perfectly vertical profile for your parts, but if there is any axial runout,
                                        that
                                        edge will not be perfectly straight. Below is an image that is a visual
                                        representation of axial runout.</li>
                                </p>
                            </div>
                            <div class="col-lg-5"><img src="assets/img/week07/group_assigment/runout.jpg"
                                    class="img-fluid" alt=""></div>

                        </div><br>

                        <div class="row">
                            <div class="col-lg-6">
                                <p>
                                    <li>
                                        Radial Runout Radial runout occurs when the drill bit is not perfectly centered
                                        in the spindle. This can affect every sort of outline, whether it be interior or
                                        exterior. If it affects an interior outline, any sort of slot or interior cutout
                                        will be slightly larger than intended. This means that if something is meant to
                                        be snap fit, the slot will be too large and the joint will be too loose.
                                    </li><br>
