                <p>
                    <strong>Safety</strong> <br>
                    There are some general basic things to have in mind before and during this whole process of casting
                    and molding.
                <ul>
                    <li>Work in a well ventilated area to avoid getting all the fumes produced into your system.</li>
                    <li>Wear gloves, safety glasses and long sleeves.</li>
                    <li>If the casting material is even more nasty, wear a respirator.</li> <br>
                    <ul>
                        <div class="col-lg-5">
                            <img src="assets/img/week09/group/handling2.jpg" class="img-fluid" alt="">
                        </div>
                    </ul><br>
                    In this picture, there are the PPEs we used. Goggles, a respirator and some gloves.
                </ul>
                </p>
                <p>
                    <strong>Handling the materials</strong> <br>
                    Each material used for creating molds or final casts is based on creating a chemical reaction
                    between two compounds that get mixed. When in separate containers, these compounds won’t get into
                    any reaction on their own (although they don’t have an endless shelf life), but when mixed they’ll
                    transform. <br> <br>
                    Fast setting plastic.
                </p>
                <div class="row">
                    <div class="col-lg-5">
                        <img src="assets/img/week09/group/handling.jpg" class="img-fluid" alt="">
                    </div>
                    <div class="col-lg-1"></div>
                    <div class="col-lg-3">
                        <br><img src="assets/img/week09/group/silicone.jpg" class="img-fluid" alt="">
                    </div><br>
                    Silicone.
                </div> <br>
                <p>
                    Each material has a data sheet which gives instructions on how to use it, general safety measures to
                    take, mixing ratio’s, curing times, etc. <br> <br>
                    Each material also has a safety sheet that more elaborately describes the hazards of using it.
                    For each material there is a mixing ratio to use for the two substances. This is often given in both
                    volume (e.g. 1A:1B) or weight (e.g. 100A:130B). We used the weight ratio and made sure to use a
                    digital scale to measure it very precisely. <br> <br>
                    Each material has a pot life, which is the time you’ll have between mixing the two components and
                    when the material’s viscosity doubles (apparently). It’s used as a proxy for understanding how much
                    time you’ll have to mix and pour the material into your mold before it’s already started setting.
                    These can be as short as 2 minutes to 30 minutes and longer. <br>
                    Each material has a curing time, which is the time needed for the material to fully set (most
                    materials also have some post-curing time in a heated environment to get even better results). This
                    can range widely as well from only 20 minutes to 16 hours. <br> <br>
                    For the best mold creation you want to have as few bubbles in the material as possible.
                <ul>
                    <li>
                        While stirring always make motions that are on the horizontal plane (back and forth), and not
                        moving up and down. Stir gently, not vigorously, but stir well, you want to have the two
                        materials fully mixed and homogeneous.
                    </li>
                    <li>
                        Another technique to reduce bubbles in the final result is to pour the material into the mold
                        very slowly, with a uniform flow, and from a single spot (at the lowest point) and let the
                        material seek its level up and over the mold.
                        It’s best to let any leftover mixed material stand in its container and let it fully cure before
                        throwing it away.
                    </li>
                </ul>

                </p>
