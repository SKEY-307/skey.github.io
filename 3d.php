                        <p>
                            <br><strong>Methode 2: Photogrammetry</strong>
                        </p>
                        <p>
                            Photogrammetry is a technique that relies on the advanced computer analysis of the
                            information contained in photographs. The extracted information allows to position in 3D the
                            place where the photos were taken and to deduce the 3D scan of the subject (object,
                            environment or person.
                        </p>
