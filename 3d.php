                        <p>UnSupported</p>
                        <ul>
                            <li><a href="http://academy.cba.mit.edu/classes/scanning_printing/angle.stl">Angle</a></li>
                        </ul>
                        <p>
                            In this test, we printed a piece that had gradually increasing overhangs to test what angle
                            of departure can be used without supports. The results of the test were that we were able to
                            print to an angle of about 20 degrees before having issues with the print results. The
                            photos below indicate the results of these tests:

                        </p>
                        <ul>
                            <li><a href="http://academy.cba.mit.edu/classes/scanning_printing/free.stl">Overhang</a>
                            </li>
                        </ul>
                        <p>
                            This test looked at how long of an overhang can be used before the print begins to fail. In
                            this test, overhangs of increasing length are printed and the length at which the print
                            becomes messy is the limit. The results for our printer indicated that the limit is
                            approximately 3 mm of overhang.
                        </p>
                        <div class="col-lg-4">
                            <img src="assets/img/week05/overhang_unsup.jpg" class="img-fluid" alt="">
                        </div><br>
                        <ul>
                            <li><a href="http://academy.cba.mit.edu/classes/scanning_printing/bridging.stl">Bridging</a>
                            </li>
                        </ul>
                        <p>
                            The birdging test evaluates how far your extruder can pul a filament across a gap before
                            drooping or sagging. In this test, the length between two posts is gradually increased.
                            Interestingly, every gap was perfectly bridged.

                        </p>
                        <div class="col-lg-8">
                            <img src="assets/img/week05/bridging.jpg" class="img-fluid" alt="">
