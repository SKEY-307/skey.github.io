                                    Thickness</a></li>
                        </ul>
                        <p>
                            In this test, a piece was printed in which wall thicknesses were increased in an additive
                            manner, and the gap between two walls was also printed. The results of this test showed that
                            a minimum thickness of around 0.8mm is required before the wall will not be able to support
                            itself and begin to deform. The image below illustrates the result.

                        </p>
