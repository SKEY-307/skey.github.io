                            </li>
                        </ul>
                        <p>
                            This piece was also printed with supports. The piece is designed such that rings are printed
                            around a rod with narrower and narrower inner diameters until you get to a clearance that is
                            too small for the printer to hand causing the rings to bind. As a result with the printers,
                            we were able to free all the rings except two sets of 0.1 mm and 0.2 mm.
                        </p>
                        <div class="col-lg-8">
                            <img src="assets/img/week05/clearance_sup.jpg" class="img-fluid" alt="">
