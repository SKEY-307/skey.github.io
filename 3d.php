                        <p>
                            <strong>3D Scanning</strong>
                        </p>
                        <strong>Methode 1: Structured light</strong>
                        <p>
                            Structured light is a light whose characteristics are known (like a regular grid for
                            example) is projected on the surface to be scanned in 3D. A sensor records the deformation
                            of the light and deduces the shape of the 3D scanned surface.
                        </p>
                        <p>
                            THE EQUIPEMENT
                        </p>
                        <p>
                            To apply the method of scanning by structured light we used the scanner 3d <a
                                href="https://support.3dsystems.com/s/article/Sense-Scanner?language=en_US">Sense</a>.
                        </p>
